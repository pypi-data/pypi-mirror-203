from doji.core.utils.classes.instance import Instance


# ┌─────────────────────────────────────────────────────────────────────────────────────
# │ ASSET
# └─────────────────────────────────────────────────────────────────────────────────────


class Asset(Instance):
    """A utility class that represents assets"""

    # ┌─────────────────────────────────────────────────────────────────────────────────
    # │ CLASS ATTRIBUTES
    # └─────────────────────────────────────────────────────────────────────────────────

    # Declare type of code
    code: str

    # ┌─────────────────────────────────────────────────────────────────────────────────
    # │ __STR__
    # └─────────────────────────────────────────────────────────────────────────────────

    def __str__(self) -> str:
        """String Method"""

        # Return code
        return self.code
